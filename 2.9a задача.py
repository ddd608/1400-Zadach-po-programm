def calculate_z(x, y):
    """Вычисляет значение функции z = 2x^3 - 3.44xy + 2.3x^2 - 7.1y + 2."""
    z = 2 * (x ** 3) - 3.44 * x * y + 2.3 * (x ** 2) - 7.1 * y + 2
    return z

if __name__ == "__main__":
    # Ввод значений x и y
    x = float(input("Введите значение x: "))
    y = float(input("Введите значение y: "))
    
    # Вычисление значения функции z
    z = calculate_z(x, y)
    
    # Вывод результата
    print(f"Значение функции z при x = {x} и y = {y}: {z:.2f}")
